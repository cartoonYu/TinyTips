package com.cartoon.tinytips.util.Adapters;

public class Major{

        private String name;

        private int imageId;

        public Major(String name, int imageId) {
            this.name = name;
            this.imageId = imageId;
        }

        public String getName() {
            return name;
        }

        public int getImageId() {
            return imageId;
        }
}
